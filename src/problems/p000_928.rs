pub struct Solution;

impl Solution {
    pub fn min_malware_spread(graph: Vec<Vec<i32>>, initial: Vec<i32>) -> i32 {
        fn count(graph: &[Vec<i32>], initial: &[i32], remove_node: usize) -> usize {
            let n = graph.len();
            use std::collections::VecDeque;
            let mut que = VecDeque::new();
            let mut vis = vec![false; n];

            let mut ans = 0;

            for v in initial {
                let x = *v as usize;
                if x == remove_node {
                    continue;
                }
                vis[x] = true;
                que.push_back(x);
                ans += 1;
            }

            while let Some(x) = que.pop_front() {
                (0..n).for_each(|y| {
                    if !vis[y] && y != remove_node && graph[x][y] == 1 {
                        vis[y] = true;
                        que.push_back(y);
                        ans += 1;
                    }
                });
            }

            ans
        }

        initial
            .iter()
            .map(|v| (count(&graph, &initial, *v as usize), *v as usize))
            .inspect(|(cnt, v)| println!("v = {v}, cnt = {cnt}"))
            .min()
            .unwrap()
            .1 as i32
    }
}

#[test]
fn test1() {
    let graph = [[1, 1, 0], [1, 1, 0], [0, 0, 1]];
    let initial = vec![0, 1];
    let graph: Vec<Vec<i32>> = graph.into_iter().map(Vec::from).collect();
    assert_eq!(Solution::min_malware_spread(graph, initial), 0);
}

